package src.main.kotlin.file.format_options

/**
 * Enum class for font styles.
 */
enum class FontStyle {
    /** Normal font style. */
    NORMAL,

    /** Bold font style. */
    BOLD,

    /** Italic font style. */
    ITALIC,

    /** Underlined font style. */
    UNDERLINE,

    /** Bold and italic font style. */
    BOLD_ITALIC,

    /** Bold and underlined font style. */
    BOLD_UNDERLINE
}
